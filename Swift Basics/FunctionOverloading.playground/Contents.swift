import UIKit


func sum(number1:Double, number2:Double) {
    let ans1 = number1 + number2
    print(ans1)
}

func sum(number1:Double, number2:Double, number3:Double) {
    let ans2 = number1 + number2 + number3
    print(ans2)
}

func sum(number1:Double, number2:Double, number3:Double, number4:Double) {
    let ans3 = number1 + number2 + number3 + number4
    print(ans3)
}

sum(number1: 20, number2: 15)

sum(number1: 15, number2: 10, number3: 5)

sum(number1: 20, number2: 15, number3: 10, number4: 5)

